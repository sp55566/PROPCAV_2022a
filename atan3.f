
      FUNCTION ATAN3(Y,X)
C
C     DATA ARRAY B FOR 6D RATIONAL APPROXIMATION OF ARCTANGENT
C     SEE HART, ET AL, COMPUTER APPROXIMATIONS, WILEY, 1968, TABLE 5090
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION B(5)
      DATA B/ 2.4091196863D-01, 3.78511223745D+00,
     *          5.67707209367D+00,
     *        5.67728543216D+00, 5.67707473502D+00/

      S12=Y
      C12=X
      FD=0.0D0
      PI = DACOS(-1.D0)
      PI2 = 0.5D0 * PI
      IF (DABS(S12).LE.DABS(C12)) THEN
         U=S12/C12
         IF (C12.LT.0.0D0) FD=FD+DSIGN(PI,S12)
      ELSE
         U=-C12/S12
         FD=FD+DSIGN(PI2,S12)
      ENDIF
      UU=U*U
      FD=FD+U*((B(1)*UU+B(2))*UU+B(3))/((UU+B(4))*UU+B(5))
      ATAN3=FD
      RETURN
      END




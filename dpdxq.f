      FUNCTION DPDXQ(X1,X2,P1,P0,P2)
************************************************************************
*     DPDXQ:                                                           *
*      --- Central difference formula                                  *
*                                                                      *
*                   2      0      1                                    *
*        -DP/DX if  +------+------+  ---> X                            *
*                      x2     x1                                       *
*                                                                      *
*                   1      0      2                                    *
*         DP/DX if  +------+------+  ---> X                            *
*                      x1     x2                                       *
************************************************************************
      X1S=X1*X1
      X2S=X2*X2
      DEN=X1S*X2+X1*X2S
      DPDXQ=( X1S*(P2-P0)+X2S*(P0-P1) ) / DEN
      RETURN


      SUBROUTINE CUBEXT(S0,S1,S2,S3,A1,A2,A3,A4)
************************************************************************
*                                                                      *
*   CUBic EXTrapolation constants  for extrapolation of any function   *
*   of f(s)                                                            *
*                                                                      *
*   *-----*-----*-----*-----*                                          *
*   s0    s1    s2    s3   s=0                                         *
*                       <---s                                          *
*   f(s0)=a1*f(s1)+a2*f(s2)+a3*f(s3)+a4*f(0)                           *
*                                                                      *
*   Author Neal Fine  07-23-91                                         *
************************************************************************
C.....first constant....................................................
      T=S0*S0*S0-S0*S0*(S2+S3)+S0*S2*S3
      B=S1*(S2-S1)*(S3-S1)
      A1=T/B
C.....second constant...................................................
      T=-S0*S0*S0+S0*S0*(S3+S1)-S0*S1*S3
      B=S2*(S3-S2)*(S2-S1)
      A2=T/B
C.....third constant....................................................
      T=S0*S0*S0-S0*S0*(S2+S1)+S0*S1*S2
      B=S3*(S3-S2)*(S3-S1)
      A3=T/B
C.....fourth constant...................................................
      T=-S0*S0*S0+S0*S0*(S1+S2+S3)-S0*(S1*S2+S1*S3+S2*S3)
      B=S1*S2*S3
      A4=1.0+T/B
      RETURN
      END
